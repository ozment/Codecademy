let isNightTime = true;

isNightTime ? console.log('Turn on the lights!') // If condition evaluates to true, the first expression executes
    : console.log('Turn off the lights'); // If the condition evaluates to false, the second expression executes

let isLocked = false;
isLocked ? console.log('You will need a key to open the door')
    : console.log('You will not need a key to open the door')

let favoritePhrase = 'Love That!';
favoritePhrase === 'Love That!' ? console.log('I love that!')
    : console.log("I don't love that!");
